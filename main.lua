task.spawn(function()
	local script = __GuiScript

	local UserInputService = game:GetService("UserInputService")
	
	local gui = script.Parent
	
	local dragging
	local dragInput
	local dragStart
	local startPos
	
	local function update(input)
		local delta = input.Position - dragStart
		gui:TweenPosition(UDim2.new(startPos.X.Scale, startPos.X.Offset + delta.X, startPos.Y.Scale, startPos.Y.Offset + delta.Y), Enum.EasingDirection.Out, Enum.EasingStyle.Quint, 0.5, true)
	end
	
	gui.InputBegan:Connect(function(input)
		if input.UserInputType == Enum.UserInputType.MouseButton1 or input.UserInputType == Enum.UserInputType.Touch then
			dragging = true
			dragStart = input.Position
			startPos = gui.Position
			
			input.Changed:Connect(function()
				if input.UserInputState == Enum.UserInputState.End then
					dragging = false
				end
			end)
		end
	end)
	
	gui.InputChanged:Connect(function(input)
		if input.UserInputType == Enum.UserInputType.MouseMovement or input.UserInputType == Enum.UserInputType.Touch then
			dragInput = input
		end
	end)
	
	UserInputService.InputChanged:Connect(function(input)
		if input == dragInput and dragging then
			update(input)
		end
	end)
	
	local flyPart
	function scanAnchored(p)
		for _, a in ipairs(p:GetConnectedParts(true)) do
			if a.Anchored then
				return true
			end
		end
		return false
	end
	
	local con
	local function initfly(char, hum)
		flyPart = Instance.new("Part", game.CoreGui)
		flyPart.Anchored = true
		flyPart.CanCollide = false
		flyPart.CFrame = hum.RootPart.CFrame
		local flyVel = Vector3.zero
		local flyStep;flyStep = game:GetService("RunService").Heartbeat:Connect(function(dT)
			for _, a in ipairs(hum.Parent:GetDescendants()) do
				if a:IsA("BasePart") then
					a.AssemblyLinearVelocity = Vector3.zero
				end
			end
			if not flyPart then
				flyStep:Disconnect()
				return
			end
			if hum then
				local camera = workspace.CurrentCamera.CFrame
				local forwardVector = camera:VectorToWorldSpace(-Vector3.zAxis)
				local sideVector = camera:VectorToWorldSpace(-Vector3.xAxis)
				local vel = CFrame.lookAt(Vector3.zero, camera.LookVector * Vector3.new(1, 0, 1))
					:VectorToObjectSpace(hum.MoveDirection)
	
				flyVel = flyVel:Lerp(
					(sideVector * -vel.X + forwardVector * -vel.Z) * hum.WalkSpeed * 4,
					math.min(dT * 3, 1)
				)
	
				flyPart.Position += flyVel * dT
	
				local sx, sy, sz, m00, m01, m02, m10, m11, m12, m20, m21, m22 = CFrame.lookAt(Vector3.zero, forwardVector):GetComponents()
				local X = math.deg(math.atan2(-m12, m22))
				local Y = math.deg(math.asin(m02))
				local Z = math.deg(math.atan2(-m01, m00))
				flyPart.Rotation = Vector3.new(X, Y, Z)
	
				char:PivotTo(flyPart.CFrame)
				for _, a in ipairs(hum.Parent:GetDescendants()) do
					if a:IsA("BasePart") then
						a.AssemblyLinearVelocity = Vector3.zero
					end
				end
				local seatPart = hum.SeatPart
				if hum.Sit and (seatPart.Anchored or scanAnchored(seatPart)) then
					if flyPart then
						flyPart:Destroy()
						flyPart = nil
					end
				end
			else
				if flyPart then
					flyPart:Destroy()
					flyPart = nil
				end
			end
		end)
	end
	
